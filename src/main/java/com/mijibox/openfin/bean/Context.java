package com.mijibox.openfin.bean;

public class Context extends FinJsonBean {
	
	private Object id;
	private String name;
	private String type;

	public Context() {
	}

	public Object getId() {
		return id;
	}

	public void setId(Object id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
